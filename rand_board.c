#include "game.h"
#include <stdlib.h>

int 		**rand_board(int **i)
{
	int	 j;
	int k;

	k = 0;
	j = 0;
	while (j <=3)
	  {
	    if (i[j][k] == 0 && k <=3)
	      {
		i[j][k] = 2;
		j = 4;
	      }
	    else if (k == 4)
	      {
		k = 0;
		j++;
	      }
	    k++;

	  }
	return (i);
}
