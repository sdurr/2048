#include "game.h"

int	**rot_right(int **i)
{
	int j;
	int k;
	int bkp;

	j = 0;
	k = 3;
	while (j <= 3 && k < 4)
	{
		if ((bkp = i[j][k]) == 0)
		{
			while (--k >= 0 && i[j][k] != 0)
			{
				if (i[j][k] != bkp)
				{
					i[j][3] = i[j][k];
					i[j][k] = 0;
				}
			}
		}
		if (k == 0 && j == 3)
		{
			k++;
			j = -1;
		}
		j++;
	}
	return(i);
}
