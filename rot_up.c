#include "game.h"

int	**rot_up(int **i)
{
	int j;
	int k;
	int bkp;

	j = 0;
	k = 0;
	while (k <= 3)
	{
		if (j <= 3)
		{
			if ((bkp = i[j][k]) == 0)
			{
				while (++j <= 3 && i[j][k] != 0)
				{
					if (i[j][k] != 0)
					{
						i[j - 1][k] = i[j][k];
						i[j][k] = 0;
					}
				}
			}
			else
				j++;
		}
		else
		  {
		    k++;
		    j = 0;
		  }
	}
	return(i);
}
