#include "game.h"

int		**rot_up(int **i, int *test)
{
	int j[2];

	j[0] = 0;
	j[1] = 0;
	while (j[1] <= 3)
	{
		if (j[0] <= 3)
		{
			if (i[j[0]][j[1]] == 0)
				while (++j[0] <= 3 && i[j[0]][j[1]] != 0)
				{
					*test = 1;
					i[j[0] - 1][j[1]] = i[j[0]][j[1]];
					i[j[0]][j[1]] = 0;
				}
			else
				j[0]++;
		}
		else
		{
			j[1]++;
			j[0] = 0;
		}
	}
	return (i);
}
