#include <curses.h>
#include <stdlib.h>

void		send_digit(void)
{
	int i;

	i = rand();
	if (i % 2 == 0)
	{
		mvaddch(LINES / 8, COLS / 8, '2');
	}
		else
		mvaddch(LINES / 8, COLS / 8, '4');
}
