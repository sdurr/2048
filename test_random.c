#include <stdlib.h>

int			test_random(int **i)
{
	int j;
	int k;

	j = 0;
	k = 0;
	while (j <= 3)
	{
		while (k <= 3)
		{
			if (i[j][k] == 0)
				return (1);
			k++;
		}
		k = 0;
		j++;
	}
	exit (0);
}
